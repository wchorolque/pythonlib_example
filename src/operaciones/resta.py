def restar(a: [int, float], b: [int, float]) -> [int, float]:
    """
    Calcula la resta de dos numeros

    Parameters
    ----------
    a : [int, float]
        DESCRIPTION.
    b : [int, float]
        DESCRIPTION.

    Returns
    -------
    [int, float]
        DESCRIPTION.

    """
    return a - b